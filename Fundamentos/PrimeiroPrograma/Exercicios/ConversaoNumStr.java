public class ConversaoNumStr {
    public static void main(String[] args) {
        Integer num1 = 1000;
        System.out.println(num1.toString().length());

        int num2 = 100000;
        System.out.println(Integer.toString(num2));

        String a = "123";
        System.out.println(Integer.parseInt(a));

    }
}
