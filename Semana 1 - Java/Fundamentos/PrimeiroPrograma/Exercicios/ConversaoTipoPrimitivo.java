public class ConversaoTipoPrimitivo {
    public static void main(String[] args) {
        double a = 1;
        System.out.println(a);

        float b = (float) 1.12345679097123;
        System.out.println(b);

        int c = 126;
        byte d = (byte) c;
        System.out.println(d);
    }
}
